#include<iostream>
using namespace std;

int main()
{

    // increment dan decrement
    //precrement dan postincrement

    int a = 5;
    int b = 5;
    int c = 5;
    int d = 5;

    //posincrent
    cout<<a<<endl;
    cout<<a++<<endl;
    cout<<a<<endl;

    //premincement
    cout<<b<<endl;
    cout<<++b<<endl;
    cout<<b<<endl;

    //decrement
    cout<<c<<endl;
    cout<<c--<<endl;
    cout<<c<<endl;

    //postincrement
    cout<<d<<endl;
    cout<<--d<<endl;
    cout<<d<<endl;

    cin.get();
    return 0;
}